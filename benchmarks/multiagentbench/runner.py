import logging
import time
from typing import List, Dict, Any
from benchmarks.base import BenchmarkRunner
class MultiAgentBenchRunner(BenchmarkRunner):
    """
    Integrates MultiAgentBench to evaluate the multi-agent coding framework.
    """
    def __init__(self, mas_instance):
        """
        Args:
            mas_instance: An instance of a multi-agent system, 
            e.g. LangchainCodingMAS from coding_scenario.langchain_mas
        """
        self.mas = mas_instance
        self.logger = logging.getLogger(__name__)

    def evaluate(self, dataset: List[Dict[str, Any]]):
        """
        Evaluates the MAS against a suite of tasks.
        """
        results = []
        for task in dataset:
            task_id = task.get("task_id", "Unknown")
            prompt = task.get("prompt", "")
            setup_code = task.get("test_setup_code", "")
            teardown_code = task.get("test_teardown_code", "")
            assertion = task.get("test_assertion", "True")
            
            # Step 1: Run setup code (e.g. creating dummy csv files)
            if setup_code:
                try:
                    exec(setup_code, globals())
                except Exception as e:
                    self.logger.error(f"Setup failed for {task_id}: {e}")
                    continue
            
            # Start timer for Total Task Completion Time
            start_time = time.time()
            
            # Step 2: Execute MAS 
            # IMPORTANT: We explicitly ask the agent to execute the code
            mas_output = self.mas.answer(prompt + "\n\nMake sure to execute the code and print the result. This is required.")
            
            # End timer
            end_time = time.time()
            total_task_time = end_time - start_time
            
            # Extract output and code generated by MAS
            final_answer = mas_output.get("final_answer", "")
            generated_code = mas_output.get("writer_code", "")
            execution_output = mas_output.get("execution_output", "")
            attempts = mas_output.get("attempt", 0)
            safeguard_allowed = mas_output.get("safeguard_allowed", True)
            token_usage = mas_output.get("token_usage", {"prompt_tokens": 0, "completion_tokens": 0, "total_tokens": 0})
            
            # Calculate Conversation Turns / Messages between agents
            # Based on workflow.md and langchain_mas.py:
            # 1 iteration = Commander -> Writer (1) + Writer -> Commander (1) + Commander -> Safeguard (1) + Safeguard -> Commander (1)
            # This is roughly 4 messages per attempt, plus the final interpretation/conclusion steps.
            messages_per_attempt = 4
            base_messages = 2 # Initial receive + Final conclude
            total_messages = base_messages + ((attempts + 1) * messages_per_attempt)
            
            # Step 3: Verify output using assertion
            is_correct = False
            error_message = ""
            
            # Note: For tasks where the Commander skips execution, we can also try to evaluate 
            # the final_answer directly, or we can try to execute the code ourselves as a fallback.
            # MultiAgentBench usually requires execution output. 
            if "Execution not run" in execution_output and generated_code:
                from utils import execute_python
                fallback_out, fallback_err = execute_python(generated_code)
                if not fallback_err:
                    execution_output = fallback_out
            
            eval_env = {"output": execution_output, "code": generated_code}
            
            try:
                if eval(assertion, eval_env):
                    is_correct = True
                else:
                    error_message = f"Assertion failed: {assertion}"
            except Exception as e:
                is_correct = False
                error_message = type(e).__name__ + ": " + str(e)
                
            # Step 4: Run teardown code (cleanup files)
            if teardown_code:
                try:
                    exec(teardown_code, globals())
                except Exception as e:
                    self.logger.error(f"Teardown failed for {task_id}: {e}")
                    
            results.append({
                "task_id": task_id,
                "prompt": prompt,
                "final_answer": final_answer,
                "execution_output": execution_output,
                
                # Qualitative Metrics
                "correctness": 1.0 if is_correct else 0.0,
                "error": error_message,
                
                # Cost Metrics
                "cost_metrics": {
                    "input_tokens": token_usage.get("prompt_tokens", 0),
                    "output_tokens": token_usage.get("completion_tokens", 0),
                    "total_tokens": token_usage.get("total_tokens", 0)
                },
                
                # Time Metrics
                "time_metrics": {
                    "total_task_completion_time_seconds": total_task_time
                },
                
                # Agent Collaboration Metrics
                "collaboration_metrics": {
                    "conversation_iterations": attempts,
                    "messages_between_agents": total_messages,
                    "activated_agents": 3, # Commander, Writer, Safeguard
                    "safeguard_blocked": not safeguard_allowed
                }
            })
            
        return results
